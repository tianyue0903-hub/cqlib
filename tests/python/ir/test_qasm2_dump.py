"""
Tests for QASM2 dump (export) functionality.

Test Coverage:
- QASM2 string generation (dumps)
- QASM2 file saving (dump)
- Standard gates output verification
- Parametric gates with expressions
- Directives (measure, barrier, reset)
- Custom gate definitions
- Extended gates (crx, cry, rzz, etc.)
"""

import tempfile
import os
from cqlib.circuit import Circuit, Parameter
from cqlib.ir.qasm2 import dumps, dump


class TestQasm2Dumps:
    """Test QASM2 generation with exact output verification."""

    def test_dumps_empty_circuit(self):
        """Generate QASM for empty circuit with correct header."""
        c = Circuit(1)
        qasm = dumps(c)

        # Verify header structure exactly
        lines = qasm.strip().split("\n")
        assert lines[0] == "// This file is auto-generated by Cqlib."
        assert lines[1] == "OPENQASM 2.0;"
        assert lines[2] == 'include "qelib1.inc";'
        assert "qreg q[1];" in qasm
        # Note: creg is only created for qubits used in conditional operations

    def test_dumps_single_qubit_gates(self):
        """Generate QASM for single-qubit gates with exact verification."""
        c = Circuit(1)
        c.h(0)
        c.x(0)
        c.y(0)
        c.z(0)

        qasm = dumps(c)
        lines = [
            line.strip() for line in qasm.split("\n") if line.strip().endswith(";")
        ]

        # Find gate lines (after header and register declarations)
        gate_lines = [
            line for line in lines if line.startswith(("h ", "x ", "y ", "z "))
        ]
        assert gate_lines == ["h q[0];", "x q[0];", "y q[0];", "z q[0];"]

    def test_dumps_phase_gates(self):
        """Generate QASM for phase gates (S, Sdg, T, Tdg)."""
        c = Circuit(1)
        c.s(0)
        c.sdg(0)
        c.t(0)
        c.tdg(0)

        qasm = dumps(c)
        lines = [
            line.strip() for line in qasm.split("\n") if line.strip().endswith(";")
        ]

        gate_lines = [
            line for line in lines if line.startswith(("s ", "sdg ", "t ", "tdg "))
        ]
        assert gate_lines == ["s q[0];", "sdg q[0];", "t q[0];", "tdg q[0];"]

    def test_dumps_cnot(self):
        """Generate QASM for CNOT gate."""
        c = Circuit(2)
        c.cx(0, 1)

        qasm = dumps(c)
        assert "cx q[0],q[1];" in qasm

    def test_dumps_two_qubit_gates(self):
        """Generate QASM for two-qubit gates (CY, CZ, SWAP)."""
        c = Circuit(2)
        c.cy(0, 1)
        c.cz(0, 1)
        c.swap(0, 1)

        qasm = dumps(c)
        lines = [
            line.strip() for line in qasm.split("\n") if line.strip().endswith(";")
        ]

        gate_lines = [
            line for line in lines if line.startswith(("cy ", "cz ", "swap "))
        ]
        assert gate_lines == ["cy q[0],q[1];", "cz q[0],q[1];", "swap q[0],q[1];"]

    def test_dumps_toffoli(self):
        """Generate QASM for CCX (Toffoli) gate."""
        c = Circuit(3)
        c.ccx(0, 1, 2)

        qasm = dumps(c)
        assert "ccx q[0],q[1],q[2];" in qasm

    def test_dumps_parametric_gates(self):
        """Generate QASM for parametric rotation gates."""
        c = Circuit(1)
        c.rx(0, 0.5)
        c.ry(0, 0.3)
        c.rz(0, 0.7)

        qasm = dumps(c)
        lines = [
            line.strip() for line in qasm.split("\n") if line.strip().endswith(";")
        ]

        gate_lines = [line for line in lines if line.startswith(("rx(", "ry(", "rz("))]
        assert gate_lines == ["rx(0.5) q[0];", "ry(0.3) q[0];", "rz(0.7) q[0];"]

    def test_dumps_u_gates(self):
        """Generate QASM for U gates."""
        c = Circuit(1)
        c.u(0, 0.5, 0.3, 0.2)

        qasm = dumps(c)
        assert "u3(0.5,0.3,0.2) q[0];" in qasm

    def test_dumps_barrier(self):
        """Generate QASM with barrier."""
        c = Circuit(3)
        c.h(0)
        c.barrier([0, 1, 2])
        c.x(1)

        qasm = dumps(c)
        assert "barrier q[0],q[1],q[2];" in qasm

    def test_dumps_measurement(self):
        """Generate QASM with measurement."""
        c = Circuit(2)
        c.h(0)
        c.measure(0)
        c.measure(1)

        qasm = dumps(c)
        # Note: measurement without conditional is commented out
        # Only measurements for qubits used in conditional operations are output
        assert "// measure q[0] -> c0[0];" in qasm or "measure q[0] -> c0[0];" in qasm
        assert "// measure q[1] -> c1[0];" in qasm or "measure q[1] -> c1[0];" in qasm

    def test_dumps_reset(self):
        """Generate QASM with reset."""
        c = Circuit(2)
        c.x(0)
        c.reset(0)
        c.reset(1)

        qasm = dumps(c)
        lines = [
            line.strip() for line in qasm.split("\n") if line.strip().endswith(";")
        ]

        reset_lines = [line for line in lines if line.startswith("reset ")]
        assert reset_lines == ["reset q[0];", "reset q[1];"]

    def test_dumps_symbolic_parameters(self):
        """Generate QASM with symbolic parameters."""
        theta = Parameter("theta")
        phi = Parameter("phi")

        c = Circuit(1)
        c.rx(0, theta)
        c.ry(0, phi)

        qasm = dumps(c)
        assert "rx(theta) q[0];" in qasm
        assert "ry(phi) q[0];" in qasm

    def test_dumps_parameter_expressions(self):
        """Generate QASM with parameter expressions."""
        theta = Parameter("theta")

        c = Circuit(1)
        c.rx(0, theta + 0.5)
        c.ry(0, theta * 2)

        qasm = dumps(c)
        assert "rx(0.5 + theta) q[0];" in qasm
        assert "ry(2*theta) q[0];" in qasm


