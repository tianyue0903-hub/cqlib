class TestQasm2DumpCustomGates:
    """Test QASM2 dump for custom gates (CircuitGate)."""

    def test_dumps_simple_custom_gate(self):
        """Generate QASM for a simple custom gate."""
        # Define sub-circuit
        sub = Circuit(2)
        sub.h(0)
        sub.cx(0, 1)
        bell = sub.to_gate("bell")

        # Use in main circuit
        c = Circuit(2)
        c.circuit_gate(bell, [0, 1])

        qasm = dumps(c)
        # Should include gate definition
        assert "gate bell q0,q1 {" in qasm
        assert "h q0;" in qasm
        assert "cx q0,q1;" in qasm
        assert "}" in qasm
        # Should include usage
        assert "bell q[0],q[1];" in qasm

    def test_dumps_parameterized_custom_gate(self):
        """Generate QASM for a parameterized custom gate."""
        theta = Parameter("theta")

        # Define sub-circuit with parameter
        sub = Circuit(1)
        sub.rx(0, theta)
        my_rx = sub.to_gate("my_rx")

        # Use with fixed value
        c = Circuit(1)
        c.circuit_gate(my_rx, [0])

        qasm = dumps(c)
        assert "gate my_rx(theta) q0 {" in qasm
        assert "rx(theta) q0;" in qasm


class TestQasm2DumpFileIO:
    """Test QASM2 file output operations."""

    def test_dump_to_file(self):
        """Save circuit to QASM file and verify content."""
        c = Circuit(2)
        c.h(0)
        c.cx(0, 1)

        with tempfile.NamedTemporaryFile(mode="w", suffix=".qasm", delete=False) as f:
            temp_path = f.name

        try:
            dump(c, temp_path)

            # Read and verify content
            with open(temp_path, "r") as f:
                content = f.read()

            assert content.startswith("// This file is auto-generated by Cqlib.")
            assert "OPENQASM 2.0;" in content
            assert "h q[0];" in content
            assert "cx q[0],q[1];" in content
        finally:
            os.unlink(temp_path)

    def test_dump_creates_valid_qasm(self):
        """Verify dumped file can be parsed back."""
        from cqlib.ir.qasm2 import load

        c1 = Circuit(3)
        c1.h(0)
        c1.cx(0, 1)
        c1.cx(0, 2)

        with tempfile.NamedTemporaryFile(mode="w", suffix=".qasm", delete=False) as f:
            temp_path = f.name

        try:
            dump(c1, temp_path)
            c2 = load(temp_path)

            assert c2.num_qubits == 3
            assert len(c2) == 3
        finally:
            os.unlink(temp_path)
