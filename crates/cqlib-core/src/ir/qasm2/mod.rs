//! # OpenQASM 2.0
//!
//! This module loads and dumps the OpenQASM 2.0 circuit subset that can be
//! represented by Cqlib's [`Circuit`](crate::circuit::Circuit) IR.
//!
//! Loading supports registers, measurements, resets, barriers, standard gates,
//! user `gate` definitions, `opaque` declarations, external includes, and the
//! OpenQASM 2.0 conditional form `if (creg == integer) qop;`.
//! `include "qelib1.inc";` is handled as a built-in special include: Cqlib
//! registers the qelib1 gate names internally and does not require a local
//! `qelib1.inc` file.
//!
//! Dumping emits normalized OpenQASM 2.0 with `include "qelib1.inc";`, `qreg`
//! and `creg` declarations, generated gate definitions, and operations. It
//! rejects features that OpenQASM 2.0 cannot represent losslessly, including
//! else branches, loops, switches, nested control flow, general classical
//! assignment, conditional barriers, and indexed-bit conditions such as
//! `if (c[0] == 1)`.
//!
//! ## Entry Points and Errors
//!
//! - [`load::load`] / [`load::from_path`] reads an OpenQASM 2.0 file and
//!   returns [`load::QasmParseError`] on failure.
//! - [`load::loads`] / [`load::from_str`] reads OpenQASM 2.0 source from a
//!   string and returns [`load::QasmParseError`] on failure.
//! - [`dump::dump`] / [`dump::to_path`] writes a circuit to an OpenQASM 2.0 file
//!   and returns [`dump::QasmDumpError`] on failure.
//! - [`dump::dumps`] / [`dump::to_string`] serializes a circuit to an OpenQASM
//!   2.0 string and returns [`dump::QasmDumpError`] on failure.
//!
//! ## Load Example
//!
//! ```rust
//! use cqlib_core::ir::qasm2::load::loads;
//!
//! let qasm = r#"
//!     OPENQASM 2.0;
//!     include "qelib1.inc";
//!     qreg q[2];
//!     creg c[1];
//!     h q[0];
//!     cx q[0], q[1];
//!     measure q[0] -> c[0];
//! "#;
//!
//! let circuit = loads(qasm).unwrap();
//! assert_eq!(circuit.num_qubits(), 2);
//! ```
//!
//! ## Dump Example
//!
//! ```rust
//! use cqlib_core::ir::qasm2::dump::dumps;
//! use cqlib_core::circuit::Circuit;
//! use cqlib_core::circuit::Qubit;
//!
//! let mut circuit = Circuit::new(2);
//! circuit.h(Qubit::new(0)).unwrap();
//! circuit.cx(Qubit::new(0), Qubit::new(1)).unwrap();
//!
//! let qasm = dumps(&circuit).unwrap();
//! assert_eq!(
//!     qasm,
//!     concat!("// This file is auto-generated by Cqlib v", env!("CARGO_PKG_VERSION"), ".\n\
//! OPENQASM 2.0;\n\
//! include \"qelib1.inc\";\n\
//! \n\
//! qreg q[2];\n\
//! \n\
//! \n\
//! \n\
//! h q[0];\n\
//! cx q[0],q[1];\n")
//! );
//! ```

